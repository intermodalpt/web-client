		<h1 id="title" class="text-2xl font-bold">Diálogos para a Mobilidade</h1>
		<div class="flex flex-col gap-2">
			<p>
				O Intermodal é um projeto com interesse na melhoria da mobilidade e das boas práticas de uso
				do espaço público. Não sendo ambientalistas no sentido organizacional, somos empáticos com
				as causas. Nesta índole, o projeto procura não tomar posições ideológicas sem um sustento
				forte por parte de factos.
			</p>
			<p>
				Temos vindo a auscultar as demais iniciativas para a mobilidade e sustentabilidade no
				panorama nacional a fim de criar uma base de conhecimento sólido. Não obstante da boa fé das
				iniciativas, foi possível observar uma grande variedade de conclusões quanto a pontos-chave
				na política nacional. Mais que diversas, as opiniões observadas tendem a apresentar alguma
				contrariedade, a qual acreditamos surgir da insuficiência de debate.
			</p>
			<p>
				Deixamos então a proposta para que grupos e indivíduos versados nas temáticas relacionadas
				tenham dialoguem. O intuito é de partilha construtiva de factos e visões que levam cada
				iniciativa ás conclusões apresentadas.
			</p>
			<h2 id="metodologia" class="text-xl font-bold">Metodologia</h2>
			<p>
				Sendo o objetivo a criação de um ambiente propicio para a partilha construtiva, propomos a
				seguinte metodologia:
			</p>
			<ul class="list-disc ml-4">
				<li>
					Levanta-se em clareza as posições de cada entidade; por exemplo, enumerando as suas
					publicações ou destacando fontes de conhecimento.
				</li>
				<li>
					Dá-se a todos a oportunidade de estudar os fundamentos uns dos outros e de retificar
					vazios no próprio conhecimento.
				</li>
				<li>
					Apresentam-se insuficiências que poderão levar cada entidade a desenvolver respostas
					esclarecedoras.
				</li>
				<li>
					Realiza-se uma reunião (virtual) onde, educadamente, se agiliza a troca de impressões
					entre entidades e se responde às dúvidas identificadas nas retóricas apresentadas.
				</li>
			</ul>

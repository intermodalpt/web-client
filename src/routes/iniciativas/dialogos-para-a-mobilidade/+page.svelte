			<h2 id="vazios" class="text-xl font-bold">Vazios nas perspetivas defendidas</h2>
			<h3 class="text-lg">Serviço convencional de longo-curso</h3>
			<p>
				É afirmado pelo GEOTA que "temos há mais de duas décadas serviços em velocidade elevada (220
				km/h), que se mostraram perfeitamente adequados" em função da crença de competitividade com
				os demais serviços. Proposta pelos mesmos é a simplificação do serviço, tornando todos os
				comboios de longo-curso em intercidades de elevada frequência.
			</p>
			<p>
				Estas afirmações são a nosso ver controversas porque temos um único serviço a operar a
				220km/h: Os Alfa-Pendulares. Este serviço (e as suas muito finitas 9 composições) só operam
				no eixo Braga - Faro, sendo essa velocidade tão infrequentemente atingida que não reflete o
				serviço. Velocidade efetiva é equiparável a um muito mais barato expresso rodoviário.
			</p>

			<p>
				O atual serviço designado como Intercidades opera com locomotivas de série 5600 (capazes de
				220km/h), todavia as suas carruagens (Corail, Sorefame ou Arco) não podem exceder os
				200km/h, por vezes menos. Não existe material em Portugal que permita o serviço defendido
				pelo GEOTA e não é claro qual o material que desejam adquirir. Este material é
				particularmente económico por ser antigo ou reaproveitado (como é o caso das Arco que foram
				adquiridas em segunda mão, procedendo a ser remodeladas). Defende o GEOTA aquisição de
				material novo para 220km/h? Material mais capaz que seja subutilizado tal que possamos mudar
				a postura posteriormente? Quão distintos prevê o GEOTA serem os custos entre estas classes
				de material?
			</p>
			<p>
				Essas velocidades atualmente apenas são atingidas por breves instantes. Apenas uma fração
				das linhas atuais permite circulações a &gt;=200km/h. Aproximadamente 30% da linha do norte,
				50% da linha do sul e uma pequena fração da linha de Évora. Apesar destas limitações, os
				serviços sofrem inúmeros atrasos (inclusive em linhas recentes) difíceis de explicar sem ser
				pelo tráfego ferroviário suburbano, regional e de mercadorias.
			</p>
			<p>
				A proposta de "aumento para o dobro das estações intercidades e aumento muito significativo
				de frequências em todas as linhas" aparenta ser positiva. Serviço ferroviário massificado é
				sem grande ambiguidade melhor para o ambiente. Todavia a duplicação das estações e aumento
				das frequências levará certamente a muito mais circulações em linhas que caso não estejam
				saturadas, passarão a estar.
			</p>
			<p>
				Fica então pouco claro como é que o GEOTA propõe que a velocidade do serviço suba para
				220km/h em troços cuja geometria não o permite e como propõe evitar uma cadeia sem fim de
				atrasos quando o Porto, Lisboa e Faro se tornarem ponto-de-partida para substancialmente
				mais comboios de longo-curso. Mesmo mencionando quadruplicações de via e duplicação de
				alguns troços. Atualmente não é apresentada uma resposta completa ao problema.
			</p>
			<p>
				Pelo mesmo motivo que o GEOTA defende que segmentos isolados de LAV não refletem os ganhos
				da linha completa, não é também possível que segmentos isolados de linha duplicada ocultem
				potenciais benéficos que uma duplicação de eixos (mesmo que em linha convencional) traria?
				Ie. uma linha para estes novos intercidades propostos (a 220km/h) e a atual para suburbanos,
				uma minoria de intercidades e serviços de mercadorias? Seria o impacto desta hipotética
				linha em relação a uma LAV resumido por facilitismos ao nível das restrições geométricas?
				Nessa eventualidade, não compensaria optar por traçados de via aptos a LAV (mesmo que não
				construidos inicialmente enquanto LAV)?
			</p>
			<h3 class="text-lg">Coexistência com Expressos Rodoviários</h3>
			<p>
				Sente-se que um elefante ignorado nas posições das diversas entidades é a existência de
				expressos rodoviários. É trivial adquirir um expresso para qualquer zona do país por menos
				de metade (por vezes um quarto) do preço da opção ferroviária, isto sem antecedência.
			</p>
			<p>
				Lisboa-Porto? Trivial de obter por 5€. Lisboa-Madrid? Imensa diversidade e horas de partida
				por 10-19€ (seja para qualquer um dos próximos 3 dias, seja no pico da época alta).
			</p>
			<p>
				Foi mencionado numa das análises publicadas Zero que um expresso para Madrid a 60€ vence
				ferrovia a 51€ dada a rapidez do expresso e os comodismos oferecidos. Todavia, ferrovia está
				atualmente a 58.95€ e o Expresso a 12€ (preços para hoje, Segunda-Feira 22 de Abril). É um
				banho de sangue onde esses comboios não apresentam qualquer competitividade que não para
				passageiros intermédios.
			</p>
			<p>
				Não foi encontrada nenhuma opinião negativa destas entidades perante expressos. O maior
				criticismo é serem menos amigos do ambiente que ferrovia ainda que melhores que veículos
				pessoais. São no entanto um serviço que possivelmente impede a economia de escala na
				ferrovia.
			</p>
			<p>
				Não é pública a rede de fluxos dos expresso em Portugal, todavia, a maior operação
				(Rede-Expressos) anuncia mais de 11 milhões de passageiros no ano passado. Existem evidência
